public class Square {
    public static void main(String[] args) {
        //rows
        for (int i = 1; i <=4 ; i++) {
            //stars
            for (int j = 1; j <=4 ; j++) {
                System.out.print("* ");
            }
            //new line
            System.out.println();
        }
    }
}
